export function getLanguage() {
    const saved = window.localStorage.getItem(STORAGE_KEY);
    return translations[saved] ? saved : "en";
}

export function setLanguage(language) {
    const nextLanguage = translations[language] ? language : "en";
    window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    document.documentElement.lang = nextLanguage;
    return nextLanguage;
}

export function t(key, fallback = key, vars = {}) {
    const language = getLanguage();
    const template = translations[language]?.[key] ?? translations.en?.[key] ?? fallback;
    return Object.entries(vars).reduce((text, [name, value]) => {
        return text.replaceAll(`{${name}}`, String(value));
    }, template);
}
